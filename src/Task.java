public class Task {
    Integer id;
    TypeOfTask type;
    String name;
    StatusOfTask status;
    String description;

    public Task(Integer id, TypeOfTask type, String name, StatusOfTask status, String description) {
        this.id = id;
        this.type = type;
        this.name = name;
        this.status = status;
        this.description = description;
    }




}
